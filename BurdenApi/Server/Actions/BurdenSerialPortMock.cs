            { "SA(.*);(.*);((.*);(.*));((0x[0-7][0-9a-fA-F]);(0x[0-7][0-9a-fA-F]);(0x[0-7][0-9a-fA-F]);(0x[0-7][0-9a-fA-F]));(.*)", (m) => {
