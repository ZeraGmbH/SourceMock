            // Set the calibration of the FETs - current values.
            { "SF((0x[0-7][0-9a-fA-F]);(0x[0-7][0-9a-fA-F]);(0x[0-7][0-9a-fA-F]);(0x[0-7][0-9a-fA-F]))", (m) => {
                _replies.Enqueue("SFACK");
