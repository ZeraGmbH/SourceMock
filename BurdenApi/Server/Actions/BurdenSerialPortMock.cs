    /// <summary>
    /// Outgoing messages.
    /// </summary>
    private readonly Queue<QueueEntry> _replies = new();

    /// <inheritdoc/>
    public void Dispose()
    {
    }

    /// <inheritdoc/>
    public byte? RawRead() => throw new NotSupportedException();

    /// <inheritdoc/>
    public void RawWrite(byte[] command) => throw new NotSupportedException();

    /// <inheritdoc/>
    public string ReadLine()
    {
        if (!_replies.TryDequeue(out var info))
        {
            Thread.Sleep(100);

            throw new TimeoutException("no reply in queue");
        }

        if (info.Delay > 0) Thread.Sleep(info.Delay);

        return info.Reply;
    }

    /// <inheritdoc/>
    public void WriteLine(string command)
    {
        foreach (var test in _Commands)
        {
            var regex = new Regex($"^{test.Key}$");
            var match = regex.Match(command);

            if (match.Success)
            {
                test.Value(match);

                break;
            }
        }
    }
